void initeyedisplay() {
  if (!display.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDRESS)) {
    Serial.println(F("SSD1306 allocation failed"));
    for (;;);
  }

  display.display();
  delay(2000);
  display.clearDisplay();
}

void eyeblink() {
  unsigned long currentMillis = millis();

  if (eyeIsOpen && !isBlinking) {
    if (currentMillis - previousMillis >= openEyeDuration) {
      previousMillis = currentMillis;
      isBlinking = true;
      blinkStep = 0;
    }
    display.clearDisplay();
    display.drawBitmap(0, 0, eyeOpen, SCREEN_WIDTH, SCREEN_HEIGHT, WHITE);
    display.display();
  } 
  else if (isBlinking) {
    if (currentMillis - previousMillis >= blinkStepDelay) {
      previousMillis = currentMillis;

      if (blinkStep <= 10) {
        display.clearDisplay();
        display.drawBitmap(0, 0, eyeClosed, SCREEN_WIDTH, SCREEN_HEIGHT, WHITE);
        display.display();
        blinkStep++;
      } else if (blinkStep <= 20) {
        display.clearDisplay();
        display.drawBitmap(0, 0, eyeOpen, SCREEN_WIDTH, SCREEN_HEIGHT, WHITE);
        display.display();
        blinkStep++;
      } else {
        isBlinking = false;
        eyeIsOpen = true;
        previousMillis = currentMillis;
      }
    }
  }
}

